void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	c;

	i = -1;
	while (++i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			c = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = c;
			i = -1;
		}
	}
}
